import type { CLevel, Register } from './types';
import { toReadingUnits } from '../learn/kanaReading';

export interface SceneSentence {
  id: string;
  kana: string;
  kanji?: string;
  displayKana?: string;
  romaji: string;
  korean: string;
  register: Register;
  speaker: 'clerk' | 'me';
  tier?: 1 | 2 | 3;
  tip?: string;
}

type Seed = [kana: string, kanji: string, korean: string, speaker: 'clerk' | 'me', tier?: 1 | 2 | 3, tip?: string];
type SentenceScene = Exclude<CLevel, 'C0'>;

const romaji = (kana: string) => {
  const units = toReadingUnits(kana);
  let out = '';
  let geminate = false;
  for (const unit of units) {
    if (unit.text === 'っ' || unit.text === 'ッ') {
      geminate = true;
      continue;
    }
    if (unit.text === 'ー') {
      const vowel = [...out].reverse().find((ch) => 'aeiou'.includes(ch));
      if (vowel) out += vowel;
      continue;
    }
    if (!unit.romaji) continue;
    out += geminate ? `${unit.romaji[0]}${unit.romaji}` : unit.romaji;
    geminate = false;
  }
  return out;
};
const sentence = (scene: string, n: number, [kana, kanji, korean, speaker, tier = 1, tip]: Seed): SceneSentence => ({
  id: `ss_${scene.toLowerCase()}_${String(n).padStart(2, '0')}`,
  kana, kanji: kanji === kana ? undefined : kanji, romaji: romaji(kana), korean,
  register: speaker === 'clerk' ? 'receptive' : 'productive', speaker, tier, tip,
});

