// 전역 kana 집합 (specific + supplemental)
const all = new Set();
for (const seeds of [...Object.values(specific), ...Object.values(supplemental)]) {
  for (const s of seeds) all.add(s[0]);
}

const PURE = /^[ぁ-ゖァ-ヺー]+$/; // 가나 + 장음만 (순수 읽기)
let added = 0;
const problems = [];
for (const [scene, seeds] of Object.entries(ADD)) {
  for (const seed of seeds) {
    const [kana, , korean, , level] = seed;
    if (all.has(kana)) { problems.push(`중복: ${scene} ${kana}`); continue; }
    if (!PURE.test(kana)) { problems.push(`비순수 kana: ${scene} ${kana}`); continue; }
    if (level === 2 && kana.length < 16) { problems.push(`길이부족(L3 안 됨, ${kana.length}자): ${scene} ${kana}`); continue; }
    specific[scene].push(seed);
    all.add(kana);
    added++;
  }
}

if (problems.length) {
  console.log('⚠️ 문제 항목 (건너뜀):');
  for (const p of problems) console.log(' -', p);
}

// 보기 좋게 저장 (시드 1줄)
const fmt = (obj) => {
  const keys = Object.keys(obj);
  let out = '{\n';
  keys.forEach((k, i) => {
    out += `  ${JSON.stringify(k)}: [\n`;
    out += obj[k].map((s) => `    ${JSON.stringify(s)}`).join(',\n');
    out += `\n  ]${i < keys.length - 1 ? ',' : ''}\n`;
  });
  return out + '}\n';
};
fs.writeFileSync(FILE, fmt(specific));
console.log(`✅ ${added}개 시드 추가 완료 (문제 ${problems.length}건)`);
